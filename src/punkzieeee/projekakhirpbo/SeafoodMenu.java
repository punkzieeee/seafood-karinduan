    private void cgta_cFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cgta_cFocusLost
        if(tampungMenu.contains(cgta_m)){
            tampungMenu.remove(tampungMenu.indexOf(cgta_m));
            cgta_m[1] = cgta_c.getText();
            tampungMenu.add(cgta_m);
        }else if(cgta_c.getText().equals("") || Integer.parseInt(cgta_c.getText()) < 1){
            JOptionPane.showMessageDialog(null, "Mau pesan berapa?", "Warning!", JOptionPane.WARNING_MESSAGE);
        }else{
            cgta_m[1] = cgta_c.getText();
            tampungMenu.add(cgta_m);
        }
    }//GEN-LAST:event_cgta_cFocusLost

